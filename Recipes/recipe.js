function makechickensoup() {
    selectrecipe.innerHTML = `
    <h1>Ingrediants</h1>
    <ol>
    <li>1 whole chicken (about 4 pounds), cut into pieces (including back)</li>
    <li>8 cups water</li>
    <li>Coarse salt</li>
    <li>3 medium onions, thinly sliced (4 cups)</li>
    <li>2 celery stalks, sliced crosswise 1/4 inch thick</li>
    <li>4 garlic cloves, crushed</li>
    <li>6 medium carrots, sliced 1/2 inch thick</li>
    </ol>
    <br>
    <h1>Steps</h1>
    <ol>
    <li>Bring chicken, water, and 1 tablespoon salt to a boil in a large stockpot. Skim foam. Add onions, celery, and garlic. Reduce heat. Simmer, partially covered, for 30 minutes.</li>
    <li>Remove breast, and set aside. Add carrots. Simmer, partially covered, for 40 minutes.</li>
    <li>Remove remaining chicken; discard back and wings. Let cool slightly. Remove meat from bones, and cut into bite-size pieces.</li>
    <li>Stir in desired amount of chicken; reserve the rest for another use. Skim fat. Season with salt.</li>
    </ol>
    `
}
function makegrapejuice() {
    selectrecipe.innerHTML = `
    <h1>Ingrediants</h1>
    <ol>
    <li>1 Cup black grapes</li>
    <li>sugar (as per taste)</li>
    <li>water (as required)</li>
    <li>1 bowl ice water</li>
    </ol>
    <br>
    <h1>Steps</h1>
    <ol>
    <li>Boil water with sugar and add grapes to it. Boil till the skin of the grapes gets cracked. Strain the water and keep aside.</li>
    <li>Transfer the blanched grapes in ice water immediately to keep the colour intact.</li>
    <li>De-skin the grapes and keep the skin aside. Refrigerate the pulp for at least an hour.</li>
    <li>Now, boil the grape skin in grape water (that was kept aside) on medium to low flame for at least 15 minutes.</li>
    <li>Strain the water and refrigerate till the juice turns chilled.6.Now, mix the pulp and juice and serve.</li>
    </ol>
    `
}
